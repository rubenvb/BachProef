%% plotF2 - script to plot F2 as in G-B thesis

F2 = importdata( 'F2.txt' );
plotopt = { '-r', '-b', '-k', '-c', '-y', '-g' };
figure( 'Name', 'F2', 'NumberTitle', 'Off' );
semilogx( F2(:,1),F2(:,2), ...
          F2(:,1),F2(:,3), ...
          F2(:,1),F2(:,4), ...
          F2(:,1),F2(:,5), ...
          F2(:,1),F2(:,6), ...
          F2(:,1),F2(:,7) );
grid on;axis( [1e-5 1e-2 0 2 ] );
xlabel( 'x' ); ylabel( 'F2' );
legend( 'Q²=1.5', 'Q²=2', 'Q²=2.5', 'Q²=3.5', 'Q²=4.5', 'Q²=5' );
